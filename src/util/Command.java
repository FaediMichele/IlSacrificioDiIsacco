package util;

/**
 * This enum is the input that is used.
 */
public enum Command {
    /**
     * The command for exit.
     */
    EXIT,

    /**
     * The command for confirm.
     */
    ENTER,

    /**
     * The command for go up.
     */
    KEY_UP,

    /**
     * The command for go right.
     */
    KEY_RIGHT,

    /**
     * The command for go down.
     */
    KEY_DOWN,

    /**
     * The command for got left.
     */
    KEY_LEFT,

    /**
     * The command for shoot up.
     */
    ARROW_UP,

    /**
     * The command for shoot right.
     */
    ARROW_RIGHT,

    /**
     * The command for shoot down.
     */
    ARROW_DOWN,

    /**
     * The command for shoot left.
     */
    ARROW_LEFT,

    /**
     * The command for interact.
     */
    INTERACT,

    /**
     * The command for shoot the bomb.
     */
    BOMB,

    /**
     * The command for show the map of the floor.
     */
    SHOWMAP, 

    /**
     * The command to make the options menu appear.
     */
    OPTIONS,

    /**
     * The command for the full screen.
     */
    FULLSCREEN
}
